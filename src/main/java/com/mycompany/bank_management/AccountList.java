    private void populateTable() {
        DBUtil dbUtil = new DBUtil();
        List<User> users = dbUtil.getAccountData();
        updateTableView(users);
    }
    
    
    private void updateTableView(List<User> users) {

        Object[][] dataArray = new Object[users.size()][5];

        // Populate the array with data from the list
        for (int i = 0; i < users.size(); i++) {
            User user = users.get(i); 
            dataArray[i][0] = user.getAccount_no();
            dataArray[i][1] = user.getUserName();
            dataArray[i][2] = user.getAccount_balance();
            
        }
        accountListTable.setModel(new javax.swing.table.DefaultTableModel( 
                dataArray,
                new String[]{ "Account No", "User Name","Account Balance"}));
    }
