    private void getTransactionHistory(){
        LinkedList<TransactionRequest> transactionHistory = Constants.approvedTransactions;
        
        while(!transactionHistory.isEmpty()){
            TransactionRequest request = transactionHistory.poll();
            int account_no = request.getAccountNo();
            double transaction_amount = request.getTransactionAmount();
            
             // Create JLabels or JTextFields to display the data
            JLabel accountLabel = new JLabel("Account Number:");
            JLabel amountLabel = new JLabel("Transaction Amount:");
            JLabel accountValueLabel = new JLabel(String.valueOf(account_no));
            JLabel amountValueLabel = new JLabel(String.valueOf(transaction_amount));

            // Add the labels to the panel
            add(accountLabel);
            add(accountValueLabel);
            add(amountLabel);
            add(amountValueLabel);
        }
    }
   
