     private void getRecentActivity(){
        Stack<recentActivity> recentActivities = Constants.recentActivitiesStack;
        
        while(!recentActivities.isEmpty()){
            recentActivity activity = recentActivities.pop();
            String activity_type = activity.getActivityType();
            Date time_stamp = activity.getTimestamp();
            
             // Create JLabels or JTextFields to display the data
            JLabel activity_typeLabel = new JLabel("Activity Type:");
            JLabel time_stampLabel = new JLabel("Date:");
            JLabel activity_typeValueLabel = new JLabel(String.valueOf(activity_type));
            JLabel time_stampValueLabel = new JLabel(String.valueOf(time_stamp));

            // Add the labels to the panel
            add(activity_typeLabel);
            add(activity_typeValueLabel);
            add(time_stampLabel);
            add(time_stampValueLabel);
        }
    }
  
